package Aula2.Exercicios;

import java.util.Scanner;

public class Exercicio2 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner();

        System.out.println("Qual é a quantidade de horas trablhadas? ");
        int horastrabalhadas = scanner.nextInt();

        System.out.println("Qual é o valor por hora? ");
        int valorporhora = scanner.nextInt();

        int total;

        valorporhora * horastrabalhadas = total;

        System.out.println("O valor a receber é " + total);

    }
}
